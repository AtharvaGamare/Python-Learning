# \n - new line
# \t - tab
# \\ - add back slash 
# \' - add single quote to a single quoted string

print("Good Morning, \nHow are you?")

print("John 20")
print("John\t20")

print("new\old") # this syntax should give a warning but it is not
print("new\\old")

print('This is a class for Python\'s students')
